#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	len;

	if (!dst && !src && !size)
		return (0);
	len = ft_strlen(dst) + ft_strlen(src);
	if (size == 0)
		return (len);
	j = 0;
	i = ft_strlen(dst);
	while (src[j] && i < size - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (len);
}
